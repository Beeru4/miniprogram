package net.xc.pojo;

/**
 * 房子类型
 */
public class HouseType {

    private Integer id;
    private String typeName;

    public HouseType() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
}
